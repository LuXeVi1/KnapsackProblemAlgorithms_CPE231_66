#ifndef MATH_UTILS_H
#define MATH_UTILS_H

// Function prototypes for mathematical utilities

/**
 * Computes the maximum of two integers.
 * @param a First integer.
 * @param b Second integer.
 * @return The maximum of the two integers.
 */
int max(int a, int b);

/**
 * Computes the minimum of two integers.
 * @param a First integer.
 * @param b Second integer.
 * @return The minimum of the two integers.
 */
int min(int a, int b);

#endif // MATH_UTILS_H
